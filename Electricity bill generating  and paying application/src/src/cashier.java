    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    	int pass;
    	String name="",month="";
    	float am=0;
    	String pass2=jTextField2.getText();
    	if("".equals(pass2)|| (pass2.contains("[a-zA-Z]") == true)) {
    		pass=0;}
    	else {
		  pass=Integer.parseInt(pass2);}
    	
    	if (0==pass)
		{
			JOptionPane.showMessageDialog(null,"! Enter correct regestration number");
		}
		else
		{
    	  try 
		  {
			Class.forName("com.mysql.jdbc.Driver");  
			Connection con=DriverManager.getConnection("jdbc:mysql://localhost/electricity_board","root","");
			String query="SELECT * FROM user ";
			Statement st;
			ResultSet rs;
				st=con.createStatement();
				rs=st.executeQuery(query);
				int flag=0;
				while(rs.next()) {
					if(rs.getInt("U_regno")==pass) {
						flag=1;
						name=rs.getString("Uname");
						break ;
						}
				}
				try 
				  {
					String query1="SELECT * FROM bill WHERE U_regno="+pass+"";
					Statement st1;
					ResultSet rs1;
						st1=con.createStatement();
						rs1=st1.executeQuery(query1);
						int flag1=0,f=-1;
						while(rs1.next()) {
							if(rs1.getString("status").equalsIgnoreCase("unpaid")) {
								//JOptionPane.showMessageDialog(null,""+am);
									am+=rs1.getFloat("amount");
									f++;
								flag1=1;
								}
						}
						if(flag1==1) {
							am+=(500.0*f);
						}

					//con.close();
				   } catch (Exception e) {
					  JOptionPane.showMessageDialog(null,e);
				     }
				if(flag==1) {
					JOptionPane.showMessageDialog(null,"User data has been loaded");
					con.close();
					if(am<=0)
						month="PAID";
					else
						month="UNPAID";
					
					cashier1 ui=new cashier1(pass,am,name,month);
			        ui.setVisible(true);
			        jTextField2.setText(null);
			        this.setVisible(false);
				}	
				else {
					JOptionPane.showMessageDialog(null,"! Please check your user's regestration number");
				}

			con.close();
		   } catch (Exception e) {
			  JOptionPane.showMessageDialog(null,e);
			  jTextField2.setText(null);
		     }
		}
    	
    }                                        

